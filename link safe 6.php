This discount can be pretty significant. Since most accidents can raise your rate by 40% for 3 years the potential savings could be several hundreds of dollars over that 3 year period. But when you switch companies, you lose this credit you've built up. If you have an accident with that new company how much are you going to regret not having that accident forgiveness by seeing your rates jacked up by 40%?<br />
