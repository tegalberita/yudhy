If you've been with one company for many years and they offer a credit that waives the first accident you have, you may want to stay or see if the other company can match it. Sometimes this is referred to as good driver discount or a longevity discount or accident forgiveness. The company rewards you for your loyalty by waiving the first accident you have.<br />
