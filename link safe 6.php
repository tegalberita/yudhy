Watch out for hidden fees. This is one that can surprise you. Some companies charge you for making monthly payments - usually $3-$5 a month. Over the course of a year that comes out to $36-$60. That one fee can take a big bite out of your potential savings so make sure you factor that into the rates you are comparing. Make sure you are really saving money when you switch.<br />
<br />
Two other things to keep in mind when shopping around for auto insurance are the new company's website and hours of operation. Make sure their hours work with your hours. If they are only open from 8-5 and you work 8-5, when are you going to be able to call them if you have a question or need to make a claim? If you do all your business online you want to make sure the company you are looking at has a capable website that can help you 24 hours a day.<br />
