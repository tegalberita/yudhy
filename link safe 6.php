Another thing to be mindful of is to make sure the company you are switching to is not offering you just a teaser rate for the first 6 months to get your business and then bump you up 6 months later once they've got you on their books. Since auto insurance is a profitable industry, companies may offer you a low ball rate to get you to switch and then once they've got you increase your rates at the renewal. If the rate the new company quotes seems too goo to be true do more research. Check out insurance forums or search Google for "XYZ insurance + Reviews."<br />
